import java.util.Date;

public class AccessLog {

    private Integer id;
    private Integer uid;
    private String name;
    private Date time;

    public AccessLog() {
    }

    public AccessLog(Integer uid, String name) {
        this.uid = uid;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }
}
